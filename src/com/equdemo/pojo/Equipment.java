package com.equdemo.pojo;

public class Equipment {
    private int equid;
    private String equname;
    private String equnum;
    private String equtype;
    private String equsupplier;
    private String numofequ;
    private String price;
    private String date;

    public int getEquid() {
        return equid;
    }

    public void setEquid(int equid) {
        this.equid = equid;
    }

    public String getEquname() {
        return equname;
    }

    public void setEquname(String equname) {
        this.equname = equname;
    }

    public String getEqunum() {
        return equnum;
    }

    public void setEqunum(String equnum) {
        this.equnum = equnum;
    }

    public String getEqutype() {
        return equtype;
    }

    public void setEqutype(String equtype) {
        this.equtype = equtype;
    }

    public String getEqusupplier() {
        return equsupplier;
    }

    public void setEqusupplier(String equsupplier) {
        this.equsupplier = equsupplier;
    }

    public String getNumofequ() {
        return numofequ;
    }

    public void setNumofequ(String numofequ) {
        this.numofequ = numofequ;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
